// Map にしておく。文字 → 置換先を O(1) で引く。
const CJK_NORMALIZE: ReadonlyMap<string, string> = new Map(CJK_PAIRS)

// 認識文字列を日本語優先へ寄せる。対象文字だけ置換し、それ以外
// (かな・英数字・記号・元から日本語の漢字) はそのまま返す。
export function normalizeToJapanese(text: string): string {
  let out = ''
  // サロゲートペアを壊さないよう、for...of でコードポイント単位に回す
  for (const ch of text) {
    out += CJK_NORMALIZE.get(ch) ?? ch
  }
  return out
}

